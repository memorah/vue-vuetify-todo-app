<template>
  <v-dialog :value="true">
    persistent max-width="290">
    <v-card>
      <v-card-title class="text-h5"> Edit Task? </v-card-title>
      <v-card-text>Edit the title of this task?</v-card-text>
      <v-text-field v-model="taskTitle" class="px-6" />
      <v-card-actions>
        <v-spacer></v-spacer>
        <v-btn color="green darken-1" text @click="$emit('close')">
          Cancel
        </v-btn>
        <v-btn color="green darken-1" text @click="saveTask"> Save </v-btn>
      </v-card-actions>
    </v-card>
  </v-dialog>
</template>

<script>
export default {
  props: ["task"],
  data() {
    return {
      taskTitle: null,
    };
  },
  methods: {
    saveTask() {
      let payload = {
        id: this.task.id,
        title: this.taskTitle,
      };
      this.$store.commit("updateTaskTitle", payload);
    },
  },
  mounted() {
    this.taskTitle = this.task.title;
  },
};
</script>

